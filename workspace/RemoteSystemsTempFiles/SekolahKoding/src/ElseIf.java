public class ElseIf {
 public static void main(String[] args){
     int x = 90;
     
     if(x < 10){
         System.out.println("x lebih kecil dari 10");
     }else if(x<60){
         System.out.println("x lebih kecil dari 60");
     }else{
         System.out.println("x lebih besar dari 60");
     }
 }   
}
